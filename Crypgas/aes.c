/* Rcon */
static const uint8_t Rcon[11] = {0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36};

/* --- Funções auxiliares --- */
static void KeyExpansion(const uint8_t* key, uint8_t* roundKeys) {
    memcpy(roundKeys, key, 16);
    uint8_t temp[4];
    int i = 16;
    int rconIter = 1;
    while (i < 176) {
        memcpy(temp, &roundKeys[i - 4], 4);
        if (i % 16 == 0) {
            // RotWord
            uint8_t k = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = k;
            // SubWord
            for (int j = 0; j < 4; j++) temp[j] = sbox[temp[j]];
            temp[0] ^= Rcon[rconIter++];
        }
        for (int j = 0; j < 4; j++) {
            roundKeys[i] = roundKeys[i - 16] ^ temp[j];
            i++;
        }
    }
}

void aes_init(aes_ctx_t *ctx, const uint8_t *key, size_t key_len) {
    (void)key_len; // sempre 16 bytes para AES-128
    KeyExpansion(key, ctx->round_keys);
}

/* --- Operações AES --- */
// Macros para operações de GF(2^8)
#define xtime(x) ((x<<1) ^ (((x>>7) & 1) * 0x1b))

static void AddRoundKey(uint8_t* state, const uint8_t* roundKey) {
    for (int i = 0; i < 16; i++)
        state[i] ^= roundKey[i];
}

static void SubBytes(uint8_t* state) {
    for (int i = 0; i < 16; i++)
        state[i] = sbox[state[i]];
}

static void InvSubBytes(uint8_t* state) {
    for (int i = 0; i < 16; i++)
        state[i] = rsbox[state[i]];
}

static void ShiftRows(uint8_t* state) {
    uint8_t temp;

    // Row 1
    temp = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = temp;

    // Row 2
    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;

    // Row 3
    temp = state[3];
    state[3] = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = temp;
}

static void InvShiftRows(uint8_t* state) {
    uint8_t temp;

    // Row 1
    temp = state[13];
    state[13] = state[9];
    state[9] = state[5];
    state[5] = state[1];
    state[1] = temp;

    // Row 2
    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;

    // Row 3
    temp = state[3];
    state[3] = state[7];
    state[7] = state[11];
    state[11] = state[15];
    state[15] = temp;
}

static void MixColumns(uint8_t* state) {
    uint8_t Tmp, Tm, t;
    for (int i = 0; i < 16; i += 4) {
        t = state[i];
        Tmp = state[i] ^ state[i+1] ^ state[i+2] ^ state[i+3];
        Tm = state[i] ^ state[i+1]; Tm = xtime(Tm); state[i] ^= Tm ^ Tmp;
        Tm = state[i+1] ^ state[i+2]; Tm = xtime(Tm); state[i+1] ^= Tm ^ Tmp;
        Tm = state[i+2] ^ state[i+3]; Tm = xtime(Tm); state[i+2] ^= Tm ^ Tmp;
        Tm = state[i+3] ^ t;         Tm = xtime(Tm); state[i+3] ^= Tm ^ Tmp;
    }
}

static void InvMixColumns(uint8_t* state) {
    uint8_t a, b, c, d;
    for (int i = 0; i < 16; i += 4) {
        a = state[i];
        b = state[i+1];
        c = state[i+2];
        d = state[i+3];

        state[i]   = (uint8_t)(0x0e*a ^ 0x0b*b ^ 0x0d*c ^ 0x09*d);
        state[i+1] = (uint8_t)(0x09*a ^ 0x0e*b ^ 0x0b*c ^ 0x0d*d);
        state[i+2] = (uint8_t)(0x0d*a ^ 0x09*b ^ 0x0e*c ^ 0x0b*d);
        state[i+3] = (uint8_t)(0x0b*a ^ 0x0d*b ^ 0x09*c ^ 0x0e*d);
    }
}

/* Criptografa bloco */
void aes_encrypt_block(aes_ctx_t *ctx, const uint8_t in[16], uint8_t out[16]) {
    uint8_t state[16];
    memcpy(state, in, 16);

    AddRoundKey(state, ctx->round_keys);

    for (int round = 1; round <= 9; round++) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, ctx->round_keys + round*16);
    }

    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, ctx->round_keys + 160);

    memcpy(out, state, 16);
}

/* Descriptografa bloco */
void aes_decrypt_block(aes_ctx_t *ctx, const uint8_t in[16], uint8_t out[16]) {
    uint8_t state[16];
    memcpy(state, in, 16);

    AddRoundKey(state, ctx->round_keys + 160);

    for (int round = 9; round >= 1; round--) {
        InvShiftRows(state);
        InvSubBytes(state);
        AddRoundKey(state, ctx->round_keys + round*16);
        InvMixColumns(state);
    }

    InvShiftRows(state);
    InvSubBytes(state);
    AddRoundKey(state, ctx->round_keys);

    memcpy(out, state, 16);
}

/* --- CBC + PKCS#7 --- */
static void pkcs7_pad(uint8_t *input, uint32_t len, uint8_t *output, uint32_t *out_len) {
    uint8_t pad = AES_BLOCK_SIZE - (len % AES_BLOCK_SIZE);
    memcpy(output, input, len);
    for (uint8_t i = 0; i < pad; i++) output[len+i] = pad;
    *out_len = len + pad;
}

static uint32_t pkcs7_unpad(uint8_t *data, uint32_t len) {
    if (len == 0) return 0;
    uint8_t pad = data[len-1];
    if (pad > AES_BLOCK_SIZE) return len;
    return len - pad;
}

void aes_encrypt_cbc_padded(aes_ctx_t *ctx, const uint8_t *input, uint32_t len,
                            uint8_t *output, uint32_t *out_len, uint8_t iv[16]) {
    uint8_t buffer[1024];
    pkcs7_pad((uint8_t*)input, len, buffer, out_len);

    uint8_t prev[16];
    memcpy(prev, iv, 16);

    for (uint32_t i = 0; i < *out_len; i += 16) {
        for (int j = 0; j < 16; j++)
            buffer[i+j] ^= prev[j];

        aes_encrypt_block(ctx, buffer+i, output+i);
        memcpy(prev, output+i, 16);
    }
}

void aes_decrypt_cbc_padded(aes_ctx_t *ctx, const uint8_t *input, uint32_t len,
                            uint8_t *output, uint32_t *out_len, uint8_t iv[16]) {
    uint8_t prev[16], tmp[16];
    memcpy(prev, iv, 16);

    for (uint32_t i = 0; i < len; i += 16) {
        memcpy(tmp, input+i, 16);
        aes_decrypt_block(ctx, input+i, output+i);
        for (int j = 0; j < 16; j++)
            output[i+j] ^= prev[j];
        memcpy(prev, tmp, 16);
    }

    *out_len = pkcs7_unpad(output, len);
}
